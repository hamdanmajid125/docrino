    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $doctorUpdateArray = [
            'user_id' => $request->id,
            'available_from' => json_encode($request->available_from),
            'available_on' => json_encode($request->available_on),
            'available_to' => json_encode($request->available_to)
        ];

        Doctor::find($request->id)->update($doctorUpdateArray);
        return redirect('doctor')->with('succces', 'Doctor Updated Successfully!');
    }

